    public void loadTranbyVoucherId(Long voucherId)
    {
        List<VoucherTransaction> trans = this.tranDao.getTranscationByVoucherId(voucherId);
        DefaultTableModel model = (DefaultTableModel)this.tblTran.getModel();
        model.setRowCount(0);
        
        for(VoucherTransaction tran : trans)
        {
            Object row[] = new Object[4];
            row[0] = tran.getId();
            row[1] = tran.getStockName();
            row[2] = tran.getPrice();
            row[3] = tran.getQuantity();
            
            model.addRow(row);
        }
    }
