    public void loadVoucher()
    {
        this.vouchers = this.voucherDao.getAllVouchers();
        
        DefaultTableModel model = (DefaultTableModel)this.tblVouchers.getModel();
        
        for(Voucher voucher : vouchers)
        {
            Object row[] = new Object[2];
            row[0] = voucher.getId();
            row[1] = voucher.getDate();
            
            model.addRow(row);
        }
    }
    
